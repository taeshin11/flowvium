const messagesDir = 'messages';
let updated = 0;
for (const [lang, strings] of Object.entries(translations)) {
  const filePath = path.join(messagesDir, `${lang}.json`);
  try {
    const content = JSON.parse(fs.readFileSync(filePath, 'utf8'));
    if (!content.intelligence) { console.error(`No intelligence key in ${lang}`); continue; }
    Object.assign(content.intelligence, strings);
    fs.writeFileSync(filePath, JSON.stringify(content, null, 2) + '\n', 'utf8');
    updated++;
  } catch (e) {
    console.error(`Error updating ${lang}:`, e.message);
  }
}
console.log(`Updated ${updated} files`);
