#!/usr/bin/env node
/**
 * scripts/build-universe-search.mjs — 검색용 전체 유니버스(1210) 모듈 생성.
 *
 * 배경(2026-06-03): 홈 "기업 직접 검색 — 616개" 가 allCompanies(정적 프로필 ~637)만 검색해서
 *   모니터링 풀(1210)과 불일치. /company/[ticker] 는 임의 ticker 라이브 동작하므로 전체 풀을
 *   검색 가능하게 → 라벨도 1210 으로 정직해짐.
 *
 * 소스 우선순위(이름): company-names.json(US 실제명 ~499) → candidate meta.name(KR 한글/기타)
 *   → ticker. sector 는 candidate meta.sector.
 * 출력: src/data/universe-search.ts  (UNIVERSE_SEARCH: {ticker,name,sector}[])
 * 사용: node scripts/build-universe-search.mjs
 */
import { readFileSync, writeFileSync } from 'fs';

const cand = JSON.parse(readFileSync('data/candidate-tickers.json', 'utf8'));
const realNames = JSON.parse(readFileSync('data/company-names.json', 'utf8'));
const tickers = Array.isArray(cand.tickers) ? cand.tickers : [];
const meta = cand.meta ?? {};

const rows = [];
const seen = new Set();
for (const t of tickers) {
  const tk = String(t).toUpperCase();
  if (seen.has(tk)) continue;
  seen.add(tk);
  const m = meta[t] || meta[tk] || {};
  // US 는 실제명 우선; KR(.KS/.KQ) 은 meta.name(한글). 둘 다 없으면 meta.name → ticker.
  const isKR = /\.(KS|KQ)$/.test(t);
  // 2026-06-05: meta.name 이 산업라벨 garbage("OEM & Other"/Unknown 등)면 회사명으로 쓰지 않음(ticker 폴백).
  //   TSLA meta.name="OEM & Other" 가 UI 회사명으로 노출되던 사건. 권위명(realNames) > 정상 meta.name > ticker.
  const GARBAGE_NAME = /\b(& Other|Other|Unknown|N\/A|Industrials?|Materials?|Financials?)\b/i;
  const cleanMeta = (m.name && m.name !== tk && !GARBAGE_NAME.test(m.name)) ? m.name : null;
  const name = (!isKR && realNames[tk]) ? realNames[tk] : (cleanMeta ?? tk);
  const sector = m.sector || 'Unknown';
  rows.push({ ticker: t, name, sector });
}

const banner = `// AUTO-GENERATED by scripts/build-universe-search.mjs — DO NOT EDIT BY HAND.\n` +
  `// 검색용 전체 모니터링 유니버스(${rows.length}). candidate-tickers.json + company-names.json 에서 생성.\n` +
  `// 갱신: npm run build:universe (batch/cron sync 시 자동).\n`;
const body = `export interface UniverseSearchEntry { ticker: string; name: string; sector: string; }\n` +
  `export const UNIVERSE_SEARCH: UniverseSearchEntry[] = ${JSON.stringify(rows, null, 0)};\n`;

writeFileSync('src/data/universe-search.ts', banner + body);
console.log(`[build-universe-search] ${rows.length} 종목 → src/data/universe-search.ts`);
console.log(`  CPRT=${rows.find(r => r.ticker === 'CPRT')?.name} | 005930.KS=${rows.find(r => r.ticker === '005930.KS')?.name}`);
